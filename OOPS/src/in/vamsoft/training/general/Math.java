package in.vamsoft.training.general;

/**
 * class name is Math.
 * 
 * @author vamsoft
 * 
 */
public class Math {

  /**
   * This is the simple method sum two number. This method return integer.
   * 
   * @param x
   * @param y
   * @return
   */
  public int sum(int x, int y) {

    return x + y;
  }

  /**
   * This method returns double.
   * 
   * @param x
   * @param y
   * @return
   */

  public double sum(double x, double y) {

    return x + y;
  }

}
